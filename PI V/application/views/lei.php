                    <div id="" class="panel">
                        <div class="box-body">
                            AINOR FRANCISCO LOTÉRIO, Prefeito de Camboriú, Estado de Santa Catarina, no uso de suas atribuições legais, FAZ SABER que a Câmara de Vereadores aprovou e ele sanciona a seguinte Lei: <br><br>

                            <strike>Art. 1º Fica o Chefe do Poder Executivo Municipal de Camboriú, autorizado a conceder diárias a título de indenização das despesas de alimentação e estadia aos servidores públicos que se deslocam temporariamente a serviço da municipalidade.</strike><br><br>

                            <strike> Art. 1º Ficam os Chefes do Poder Executivo e Legislativo do Município de Camboriú, autorizados a conceder diárias a título de indenização das despesas de alimentação e estadia aos servidores públicos que se deslocam temporariamente a serviço da municipalidade. (Redação dada pela Lei nº 1658/2005)</strike><br><br>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 1º Fica concedida ajuda de custos, sob a forma de diária, aos servidores públicos do Poder Executivo e Legislativo que se deslocam temporariamente a serviço da municipalidade. (Redação dada pela Lei nº 2796/2015)</p><br><br>

                            <strike > Art. 2º As diárias são concedidas quantitativamente por período de 12 (doze) horas, requerida com antecedência de 24 (vinte e quatro) horas e creditada nominalmente pela Prefeitura ao servidor responsável, de acordo com o grau de deslocamento.</strike><br><br>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 2º As diárias são concedidas quantitativamente por período de 06 (seis) e 12 (doze) horas, requerida com antecedência de 24h (vinte quatro horas) e creditada nominalmente pelo Município ao servidor responsável, de acordo com o grau de deslocamento. (Redação dada pela Lei nº 2796/2015)</p><br><br>

                            Parágrafo Primeiro: Para deslocamento ao interior do Estado de Santa Catarina: <br>

                            I - R$ 80,00 por período de 24 horas; <br>

                            II - R$ 40,00 por período de 12 (doze) horas. <br>

                            III - R$ 20,00 (vinte reais) por período de 06 (seis) horas. (Redação acrescida pela Lei nº 2796/2015) <br><br>

                            Parágrafo Segundo: Para deslocamento a outros Estados: <br>

                            I - R$ 100,00 por período de 24 horas; <br>

                            II - 50,00 por período de 12 horas. <br><br>

                            <strike >Art. 3º Fica concedida ajuda de custos, sob forma de diárias, aos agentes públicos, destinados aos pagamentos de gastos, decorrentes do exercício do cargo, para tratarem de interesses do Município, fora de seu território, no valor de R$ 200,00 (duzentos reais), por período de 24 (vinte e quatro) horas, e de R$ 100,00 (cem reais), por período de 12 (doze) horas, até o limite de diárias mensais.</strike><br><br>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 3º Fica concedida ajuda de custos, sob forma de diária, aos agentes políticos, destinados aos pagamentos de gastos decorrentes do exercício do cargo, para tratarem de interesses do Município fora de seu território, nos seguintes valores:</p><br>

                            I - 06 (seis) UFM, por período de 12h (doze horas), em municípios com até 100km (cem quilômetros) de distância;<br>

                            II - 09 (nove) UFM, por período de 12h (doze horas), em municípios com mais de 100km (cem quilômetros) de distância;<br>

                            III - 17 (dezessete) UFM, por período de 24h (vinte e quatro horas);<br>

                            IV - 25 (vinte e cinco) UFM, por período de 24h (vinte e quatro horas), em deslocamento ao Distrito Federal-DF;<br>

                            V - $ 400 (quatrocentos dólares), por período de 24h (vinte e quatro horas), em deslocamento para o exterior do país. (Redação dada pela Lei nº 2573/2013) <br><br>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 4º A concedida de diárias será efetivada após a apresentação do respectivo roteiro de viagem, nos moldes do anexo I da presente Lei.</p><br>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 5º Não será permitido o pagamento de diárias a servidores, quando o deslocamento compreender uma distância inferior a 50 quilômetros da sede do Município, exceto para realização de curso com duração mínima de 08 (oito) horas.</p><br>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 6º Esta Lei entrará em vigor na data de sua publicação e revogam-se as disposições em contrário.</p><br>

                            PREFEITURA MUNICIPAL DE CAMBORIÚ,
                            em 15 de maio de 1995. <br><br>

                            AINOR FRANCISCO LOTÉRIO 
                            Prefeito Municipal <br><br>

