                    <div class="box-header with-border">
                        <h4 class="box-title">
                            DISPÕE SOBRE A INSTITUIÇÃO DO REGIME DE ADIANTAMENTOS DA PREFEITURA MUNICIPAL DE CAMBORIÚ.
                        </h4>
                    </div>
                    <div id="" class="panel">
                        <div class="box-body">
                            AINOR FRANCISCO LOTÉRIO, Prefeito Municipal de Camboriú, no uso das atribuições que lhe são conferidas por Lei, faz saber que a Câmara de Vereadores aprovou e ele sanciona a seguinte Lei:<br><br>

                            <strong class="pull-center">Capítulo I
                            DAS DISPOSIÇÕES PRELIMINARES</strong> <br><br>
                            
                            <p style='color:blue; font-size:15px; text-align:left'>                            
                                Art. 1º Fica instituído ao Fundo Municipal da Prefeitura Municipal de Camboriú, a forma de pagamento de despesas pelo regime de adiantamento, que reger-se-á segundo as normas legais vigentes que disciplinam a matéria.
                            </p>

                            <p style='color:blue; font-size:15px; text-align:left'>
                                Art. 2º Entende-se por adiantamento, o numerário colocado à disposição de uma Repartição, a fim de lhe dar condições de realizar despesas que, por sua natureza ou urgência, não possam aguardar o processamento normal.
                            </p>
                            
                            <p style='color:blue; font-size:15px; text-align:left'>
                                Art. 3º Os pagamentos a serem efetuados através do Regime de Adiantamento ora instituído, restringir-se-ão aos casos previstos nesta Lei e sempre em caráter de exceção.
                            </p>
                            
                            <p style='color:blue; font-size:15px; text-align:left'>
                                Art. 4º O adiantamento mensal de cada espécie de despesa não ultrapassará o valor do duodécimo da dotação correspondente.
                            </p>

                            <p style='color:blue; font-size:15px; text-align:left'>
                                Art. 5º Poderão realizar-se sob o Regime de Adiantamento os pagamentos decorrentes das seguintes espécies de despesa:
                            </p>
                            
                            I - Despesas com material de consumo; <br>
                            II - Despesas com serviços de terceiros; <br>
                            III - Despesas com diárias e e ajuda de custo; <br>
                            IV - Despesas com transportes em geral;<br>
                            V - Despesas judiciais; <br>
                            VI - Despesas com representação eventual;<br>
                            VII - Despesa extraordinária e urgente, cuja realização não permita delongas;<br>
                            VIII - Despesa que tenha de ser efetuada em lugar distante da sede da Prefeitura; <br>
                            IX - Despesa miúda e de pronto pagamento. <br>
                            V - Despesas judiciais; <br><br>

                            <p style='color:blue; font-size:15px; text-align:left'>
                                Art. 6º Considera-se despesa miúda e de pronto pagamento, para os efeitos desta Lei, as que se realizarem com:
                            </p>

                            I - selos postais, telegramas, radiogramas, material e serviços de limpeza e higiene, lavagem de roupa, café, lanche, pequenos carretos, transportes urbanos, pequenos consertos, telefone, água e luz, gás e aquisição avulsa de livros, jornais e outras publicações;<br>
                            II - encadernações avulsas e artigos de escritório, de desenho, impressos em papelaria, em quantidade restrita, para uso ou consumo próximo ou imediato;<br>
                            III - artigos farmacêuticos ou de laboratório, em quantidade restrita, para uso ou consumo próximo ou imediato;<br>
                            IV - outra qualquer, de pequeno vulto e de necessidade imediata, desde que devidamente justificada.<br><br>

                           <p style='color:blue; font-size:15px; text-align:left'>
                                Art. 7º As despesas com artigos em quantidade maior, de uso ou consumo remotos, correrão pelos itens orçamentários próprios e seguirão o processamento normal da despesa.
                            </p><br>


                            <strong> Capítulo II
                            DAS REQUISIÇÕES DE ADIANTAMENTOS </strong><br><br>
                            
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 8º As requisições de adiantamentos serão feitas pelos Secretários Municipais, através de ofício dirigido ao Chefe do Poder Executivo Municipal.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 9º Dos ofícios requisitórios de adiantamento constarão, necessariamente, as seguintes informações:</p>

                            I - Dispositivo legal em que se baseia;;<br>

                            II - Identificação da espécie da despesa mencionando o item do artigo 5º desta Lei, no qual ela se classifica;<br>

                            III - Nome completo, cargo ou função do servidor responsável pelo adiantamento;<br>

                            IV - Dotação orçamentária a ser onerada;<br>

                            V - Prazo de aplicação.<br><br>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 10º O prazo de aplicação poderá ser em base mensal, mencionando-se, neste caso, o valor global do adiantamento, a quantia mensal a ser entregue e os meses de aplicação.</p>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 11º Na hipótese de adiantamento único, o ofício requisitório deverá esclarecer este fato e fixar o prazo de aplicação.</p>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 12º Não se fará novo adiantamento:</p>
                            
                            I - A quem do anterior não haja prestado contas no prazo legal;<br>
                            
                            II - A quem, dentro de trinta dias, deixar de atender notificação para regularizar prestação de contas.<br><br>
                            
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 13º Não se fará adiantamento:</p>
                            I - Para despesa já realizada;<br>
                            
                            II - A servidor em alcance;<br>
                            III - A servidor responsável por dois adiantamentos.;<br><br>
                            
                            <strong> 
                                Capítulo III
                                DO PERÍODO DE APLICAÇÃO
                            </strong><br><br> 
                                
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 14º O adiantamento solicitado em base mensal somente poderá ser aplicado durante o mês a que se refere ou durante o período de trinta dias a contar da data da entrega do dinheiro ao responsável.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 15º No caso de adiantamento único, o período de aplicação será aquele estabelecido no ofício requisitório, conforme estabelecido no artigo 11.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 16º Nenhum pagamento poderá ser efetuado fora do período de aplicação.</p>
                            
                            <strong> 
                                Capítulo IV
                                DA TRAMITAÇÃO DOS PROCESSOS DE ADIANTAMENTO
                            </strong><br><br> 
                            
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 17º O ofício requisitório será autuado e protocolado seguindo diretamente ao Gabinete do Prefeito para a competente autorização.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 18º Os processos de adiantamento terão andamento preferencial e urgente.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 19º Autorizada, a despesa será empenhada e paga com cheque nominal a favor do responsável indicado no processo.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 20º No caso de adiantamento em duodécimos, a despesa será empenhada globalmente, pelo total do período e, mensalmente, far-se-á o pagamento correspondente. Neste caso os pagamento correrão pelo mesmo processo.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 21º Cabe ao Departamento de Contabilidade verificar, antes de registrar o empenho, se foram cumpridas as disposições desta Lei. Constatando algum defeito processual, não dará prosseguimento ao processo, devendo devolvê-lo informado, para os reparos que se fizerem necessários.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 22º Efetuado o pagamento, o Departamento de Contabilidade inscreverá o nome do responsável no Sistema de Compensação em conta apropriada, subordinada a RESPONSÁVEIS POR ADIANTAMENTOS.</p>

                            <strong> 
                                Capítulo V
                                DAS NORMAS DE APLICAÇÃO DO ADIANTAMENTO
                            </strong><br><br> 

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 23º O adiantamento não poderá ser aplicado em despesa de classificação diferente daquela para a qual foi autorizada.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 24º A cada pagamento efetuado o responsável exigirá o correspondente comprovante: nota fiscal, nota simplificada, cupom, recibo, etc.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 25º As notas fiscais serão sempre emitidas em nome da Prefeitura Municipal de Camboriú.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 26º s comprovantes de despesa não poderão conter rasuras, emendas, borrões e valor ilegível, não sendo admitido, em hipótese alguma, segundas vias, ou outras vias, cópias, xerox, fotocópias ou qualquer outras espécie de reprodução.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 27º Cada pagamento será convenientemente justificado, esclarecendo-se a razão da despesa, o destino da mercadoria ou do serviço e outras informações que possam melhor explicar a necessidade da operação.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 28º Nenhuma despesa realizada pelo Regime de Adiantamento poderá ultrapassar o valor correspondente a um salário mínimo mensal vigente.</p>

                            Parágrafo Único - Ficam excluídas do limite estabelecido neste artigo, as despesas correspondentes aos itens V, VI, VII e VIII do artigo 5º.<br> <br> 

                            <strong> 
                                Capítulo VI
                                DO RECOLHIMENTO DO SALDO NÃO UTILIZADO
                            </strong><br><br> 

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 30º O saldo de adiantamento não utilizado será recolhido mediante depósito em conta bancária do Fundo, através de guia de depósito, onde constará o nome do responsável e identificação do adiantamento, cujo saldo está sendo restituído.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 31º O prazo para recolhimento do saldo não utilizado será de 03(três) dias úteis, a contar do termo final do período de aplicação.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 32º O Departamento de Contabilidade à vista da guia de depósito, emitirá a nota de anulação correspondente, juntando uma via ao processo. Registrará a anulação no Diário da Despesa Empenhada, e no Diário na Despesa Realizada.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 33º No mês de dezembro, todos os saldo de adiantamento serão depositados até o último dia útil, mesmo que o período de aplicação não tenha expirado.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 34º Se, eventualmente, algum saldo de adiantamento for recolhido no exercício seguinte, o valor será classificado como receitas diversas do exercício.</p>
                            
                            <strong> 
                                Capítulo VII
                                DA PRESTAÇÃO DE CONTAS
                            </strong><br><br> 

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 35º No prazo de 10(dez) dias, a contar do termo final do período de aplicaçao, o responsável prestará contas da aplicação do adiantamento recebido.</p>
                            Parágrafo Único - A cada adiantamento corresponderá uma prestação de contas. <br><br>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 36º A prestação de contas far-se-á mediante entrada, no Departamento de Contabilidade, dos seguintes documentos:</p>
                           
                            I   - Ofício contendo modelo a ser elaborado pelo Departamento de Contabilidade;<br>
                            II  - Impressos conforme modelos anexos a esta Lei;<br>
                            III - Relação de todos os documentos de despesa, constando: número e data do documento, espécie de documento, nome do interessado e valor da despesa, constando no final da relação a soma da despesa realizada;<br>
                            IV  - Cópia do talão de depósito bancário do saldo não aplicado, se houver;<br>
                            V   - Cópias da Nota de Empenho e da Nota de Anulação, se houver saldo recolhido;<br>
                            VI  - Documentos das despesas realizadas, dispostos em ordem cronológica, na mesma seqüência da relação mencionada no item III;<br>
                            VII - Os documentos mencionados no item VI, de medidas reduzidas, serão colados em folhas brancas, tamanho ofício, em cada folha poderão ser colados quantos documentos forem possíveis sem que fiquem sobrepostos uns aos outros;<br>
                            VIII - Em cada documento constará, obrigatoriamente: atestado de recebimento do material ou da prestação do serviço; a finalidade da despesa; o destino do material e outros esclarecimentos que se fizerem necessários à perfeita caracterização da despesa.<br><br>

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 37º Não serão aceitos documentos rasurados, ilegíveis, com data anterior ou posterior ao período de aplicação do adiantamento ou que se refira a despesa não classificável na espécie de adiantamento concedido.</p>
                            Parágrafo Único - Somente serão aceitos documentos originais, não se admitindo outras vias, xerox, fotocópias ou outra espécie de reprodução.<br><br>
                           
                            <strong> 
                                Capítulo VIII
                                DAS DISPOSIÇÕES FINAIS
                            </strong><br><br> 

                            <p style='color:blue; font-size:15px; text-align:left'>Art. 38º Caberá ao Departamento de Contabilidade a tomada de contas dos adiantamentos.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 39º Recebidas as prestações de contas, conforme dispõe o artigo 36, o Departamento de Contabilidade verificará se as disposições da presente Lei foram inteiramente cumpridas, fazendo as exigências necessárias, fixando prazos razoáveis para que os responsáveis possam cumprí-las.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 40º Se as contas forem consideradas em ordem e boas, a Chefia do Departamento de Contabilidade certificará o fato, no local apropriado do documento mencionado no item II do artigo 36 e encaminhará o processo apensado ao que autorizou o adiantamento para exame final e parecer.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 41º Com o parecer final, o processo será encaminhado diretamente ao Chefe do Poder Executivo para aprovação ou não aprovação das contas, voltando ao Departamento de Contabilidade para as seguintes providências:</p>
                            
                            I - No caso de as contas terem sido aprovadas: <br>

                            a. baixar a responsabilidade inscrita no sistema de compensação;<br>
                            b. convidar o responsável para tomar ciência, no próprio processo;<br>
                            c. arquivar o processo de prestação de contas apenso ao processo que autorizou o adiantamento, em local seguro onde ficará à disposição do Tribunal de Contas.<br>

                            II - Na hipótese de aprovação das contas condicionadas a determinadas exigências:<br>

                            a. providenciar o cumprimento das providências determinadas;<br>
                            b. adotar as medidas indicadas no item anterior.<br>

                            III - Não tendo sido aprovadas as contas, seguir a orientação determinada pelo Prefeito em seu despacho final.<br><br>
                            
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 42º O Departamento de Contabilidade organizará um calendário para controlar as datas em que deverão entrar as prestações de contas de adiantamentos concedidos.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 43º No dia útil imediato ao vencimento do prazo para prestação de contas, sem que o responsável as tenha apresentado, o Departamento de Contabilidade oficiará diretamente ao responsável, concedendo-lhe o prazo final e improrrogável de três dias úteis para fazê-lo.</p>
                            Parágrafo Único - Na cópia do ofício o responsável assinará o recebimento da via original, colocando de próprio punho a data do recebimento. <br><br>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 44º Não sendo cumprida a obrigação da prestação de contas, após o vencimento do prazo final final estabelecido no artigo anterior, o Departamento de Contabilidade remeterá, no dia imediato, a cópia do ofício referida no parágrafo único do artigo 43 ao Departamento Jurídico, devidamente informada, para abertura de sindicância nos termos da legislação vigente.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 45º Os casos omissos serão disciplinadoso pelo Prefeito Municipal.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 46º Esta Lei entrará em vigor na data de sua publicação, revogadas as disposições em contrário.</p>
                            <p style='color:blue; font-size:15px; text-align:left'>Art. 34º Se, eventualmente, algum saldo de adiantamento for recolhido no exercício seguinte, o valor será classificado como receitas diversas do exercício.</p>
                            

                            PREFEITURA MUNICIPAL DE CAMBORIÚ,
                            em 30 de março de 1994.<br><br>
                            AINOR FRANCISCO LOTÉRIO 
                            Prefeito Municipal <br><br>

